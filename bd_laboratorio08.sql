--letra a
SELECT F.NM_FUNCIONARIO 
FROM TB_FUNCIONARIO F
WHERE F.MATRICULA NOT IN (SELECT F.MATRICULA 
		FROM TB_FUNCIONARIO F INNER JOIN TB_SOLICITACAO S
		ON (F.MATRICULA = S.MATRICULA)
		WHERE S.DATA_ABERTURA = '20190622') AND F.MATRICULA IN
		(SELECT F.MATRICULA 
			FROM TB_FUNCIONARIO F INNER JOIN TB_SOLICITACAO S
			ON (F.MATRICULA = S.MATRICULA)
			WHERE S.DATA_ABERTURA = '20190620')

--letra b
SELECT F.MATRICULA, F.NM_FUNCIONARIO, 
		(SELECT COUNT(S.CD_SOLICITACAO)
			FROM TB_SOLICITACAO S
			WHERE S.MATRICULA = F.MATRICULA AND S.DATA_ABERTURA = '20190620') AS 'DIA 20',
		(SELECT COUNT(S.CD_SOLICITACAO)
			FROM TB_SOLICITACAO S
			WHERE S.DATA_ABERTURA = '20190622') AS 'DIA 22'
FROM TB_FUNCIONARIO F

--letra c
SELECT F.NM_FUNCIONARIO, COUNT(S.CD_SOLICITACAO) TOTAL
FROM TB_FUNCIONARIO F
	LEFT OUTER JOIN TB_SOLICITACAO S ON (F.MATRICULA = S.MATRICULA)
GROUP BY F.NM_FUNCIONARIO

UNION ALL 

SELECT T.NM_TECNICO, COUNT(S.CD_SOLICITACAO) TOTAL
FROM TB_TECNICO T
	LEFT OUTER JOIN TB_SOLICITACAO S ON (T.CD_TECNICO = S.CD_TECNICO)
GROUP BY T.NM_TECNICO

ORDER BY TOTAL DESC 

--letra d
SELECT F.NM_FUNCIONARIO, COUNT(S.CD_SOLICITACAO) TOTAL
FROM TB_FUNCIONARIO F INNER JOIN TB_SOLICITACAO S
ON(F.MATRICULA = S.MATRICULA )
GROUP BY F.NM_FUNCIONARIO
HAVING COUNT(S.CD_SOLICITACAO) >= ALL (SELECT COUNT(S.CD_SOLICITACAO)
										FROM TB_FUNCIONARIO F LEFT JOIN TB_SOLICITACAO S
										ON(S.MATRICULA = F.MATRICULA)
										GROUP BY F.NM_FUNCIONARIO)

--letra e
